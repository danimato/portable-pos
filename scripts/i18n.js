function t(key) {
  const lang = translations[currentLanguage] || translations.en;
  return lang[key] !== undefined ? lang[key] : (translations.en[key] !== undefined ? translations.en[key] : key);
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.documentElement.lang = currentLanguage;
  if (typeof updatePriceLabel === 'function') updatePriceLabel();
}

async function setLanguage(lang) {
  currentLanguage = lang;
  applyTranslations();
  // Re-render inventory list header if visible
  if (typeof refreshInventoryList === 'function') refreshInventoryList();
  try {
    await db.update('settings', { setting_key: 'language', setting_value: lang });
  } catch (error) {
    console.error('Error saving language setting:', error);
  }
}
